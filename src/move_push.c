#include "push_swap.h"

void	move_push(t_stack **list_a, t_stack **list_b)
{
	t_stack	*tmp;
	if (*list_a)
	{
		tmp = *list_a;
		*list_a = (*list_a)->next;
		tmp->next = *list_b;
		*list_b = tmp;
	}
}

